num1 = input("Input Num1:")
num2 = input("Input Num2:")

add_result = float(num1) + float(num2)
sub_result = float(num1)-float(num2)
mul_result = float(num1)*float(num2)
div_result = float(num1)/float(num2)

print(num1 + " + "+ num2 + " = " + str(add_result))
print(num1 + " - "+ num2 + " = " + str(sub_result))
print(num1 + " * "+ num2 + " = " + str(mul_result))
print(num1 + " / "+ num2 + " = " + str(div_result))